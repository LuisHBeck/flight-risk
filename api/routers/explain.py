"""
api.routers.explain
===================
Router for the POST /explain/shap endpoint.

Receives SHAP values from a prediction and returns a natural-language
explanation (PT-BR) generated by Gemini via Vertex AI.

Environment variables
---------------------
GOOGLE_CLOUD_PROJECT : str
    GCP project ID.
GOOGLE_CLOUD_LOCATION : str
    Vertex AI region (e.g. ``us-central1``).
GEMINI_MODEL : str
    Gemini model ID (e.g. ``gemini-2.5-flash``).
"""

from __future__ import annotations

import os

from fastapi import APIRouter, HTTPException, status

from api.schemas import ExplanationResponse, ShapExplanationRequest

router = APIRouter(prefix="/explain", tags=["explainability"])

